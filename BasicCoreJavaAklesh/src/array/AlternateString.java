package array;

public class AlternateString {

	public static void main(String[] args) {
		// Print alternate elements of String array 
		
      String[] str = {"Rahul","Mukesh","Anand","Pratik","Manoj"};
      
      
      for(int i=0;i<=5;i+=2) 
      {
    	 
    	  System.out.println(str[i]);
    	  
      }
      
      
	}

}
