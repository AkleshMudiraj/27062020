package variablesAndDataTypes;

public class Assignment1 {

	public static void main(String[] args) {
		// Swapping two number 

		int a=10;
		int b=20;
		
		
		a=a+b;  //a=30 (10+20)
		b=a-b;  //b=10 (30-20)
		a=a-b;  //a=20 (30-10)
		
		
		
		System.out.println(a);
		System.out.println(b);
		
	}

}
